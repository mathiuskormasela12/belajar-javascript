/* 
  ==== Belajar Tipe Data String ==== 

  Tipe data string adalah tipe data
  yang berisi suatu teks ataupun
  bisa juga disebut tipe data yang
  berisi kumpulan atau single karakter.
  Contoh tipe data string adalah
  nama seseorang.

  Ada dua cara untuk menuliskan
  tipe data string :
  a. Dengan double quotes (petik dua)
  b. Dengan single quotes (petik satu)
*/
// Dengan double quotes
var huruf = "abcdefghijklmnopqrstuvwxyz";

// Dengan single quotes
var tunggal = 'tunggal';

console.log(huruf);
console.log(tunggal);
