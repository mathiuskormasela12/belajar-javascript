var a = 10;
var b = 20;

/* 
  ====== Belajar Operator Aritmatika ======

  Operator aritmatika adalah operator untuk
  melakukan operasi-operasi matematika
  seperti pertambahan, pengurangan, perkalian,
  pembagian dan sisah bagi (modulus/modulo)
*/

// Penjumlahan
console.log( a + b );

// Pengurangan
console.log( b - a );

// Perkalian
console.log( a * b );

// Pembagian
console.log( b / a );

// Sisa Bagi (modulus/modulo)
console.log( b % a );