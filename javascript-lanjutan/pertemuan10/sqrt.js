/* 
  ==== Method SQRT ====

  SQRT berfungsi untuk mencari hasil dari
  akar pangkat dua suatu bilangan.
*/
function Akar()
{
  // akar pangkat dua dari 25 = 5
  console.log( Math.sqrt( 25 ) );

  // akar pangkat dua dari 64 = 8
  console.log( Math.sqrt( 64 ) );

  // akar pengkat dua dari 81 = 9
  console.log( Math.sqrt( 81 ) );
}

Akar();