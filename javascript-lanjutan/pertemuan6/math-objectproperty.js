// Nilai dari logaritma natural e
// nilai = = 2.71xxx
console.log( Math.E );

// Nilai dari logaritma natural 2 
// nilai = 0.69
console.log( Math.LN2 );

// Nilai dari logaritma natural 10
// nilai = 2.30
console.log( Math.LN10 )

// Nilai dari logaritma e dengan basis 2
// nilai = 1.44
console.log( Math.LOG2E );

// Nilai dari logaritma e dengan basis 10
// nilai = 0.43
console.log( Math.LOG10E );

// Nilai dari phi
// nilai = 3.14
console.log( Math.PI );

// Nilai dari 1 dibagi akar kuadrat 2
// nilai = 0.70
console.log( Math.SQRT1_2 );

// Nilai dari akar kuadrat 2
// nilai = 1.41
console.log( Math.SQRT2 );